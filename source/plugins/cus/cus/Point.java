package cus;

public class Point {
   public double x;
   public double y;
   public double z;
   public float yaw;
   public float pitch;

   public Point(double x, double y, double z, float yaw, float pitch) {
      super();
      this.x = x;
      this.y = y;
      this.z = z;
      this.yaw = yaw;
      this.pitch = pitch;
   }
}
